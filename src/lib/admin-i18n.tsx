type AdminI18nContextType = {
  locale: AdminLocale;
  setLocale: (locale: AdminLocale) => void;
  t: (key: string) => string;
};

const AdminI18nContext = createContext<AdminI18nContextType>({
  locale: 'nl',
  setLocale: () => {},
  t: (key: string) => key,
});

export function AdminI18nProvider({ children }: { children: ReactNode }) {
  const [locale, setLocaleState] = useState<AdminLocale>(() => {
    if (typeof window !== 'undefined') {
      return (localStorage.getItem('admin_locale') as AdminLocale) || 'nl';
    }
    return 'nl';
  });

  const setLocale = useCallback((newLocale: AdminLocale) => {
    setLocaleState(newLocale);
    localStorage.setItem('admin_locale', newLocale);
  }, []);

  const t = useCallback((key: string) => {
    if (locale === 'en') return en[key] || key;
    return key;
  }, [locale]);

  return (
    <AdminI18nContext.Provider value={{ locale, setLocale, t }}>
      {children}
    </AdminI18nContext.Provider>
  );
}

